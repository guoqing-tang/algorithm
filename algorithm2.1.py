i =int(input('利润：'))
l1 = [1000000,600000,400000,200000,100000,0]
l2 = [0.01,0.015,0.03,0.05,0.075,0.1]
r = 0
for j in range(0,6):
    if i > l1[j]:
        r +=(i - l1[j])*l2[j]
        print((i-l1[j])*l2[j]) #这一步输出了每个区间应该的提成，可以省略，如果省略，则直接输出结果
        i = l1[j]
print(r)
